package ejercicio8;

public class Decimo {
	

	private int numero;
	
	public Decimo(int numero) {
		super();
		this.numero = numero;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	@Override
	public String toString() {
		return "Decimo [numero=" + numero + "]";
	}
	
	
	
	

}
